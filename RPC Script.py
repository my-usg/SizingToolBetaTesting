def gen_matchRPC(result, opp):
    match = None

    model_labelsRPC = {
        "RPC": '243-RPC',
        "RPCA": '243-RPC-A',
        "RPCB": '243-RPC-B',
    }

    if model_input == "243-RPC-B":
        ordered_prefixes = ['RPCB', 'RPCA']
    else:
        ordered_prefixes = ['RPCA', 'RPCB']

    # smallest to largest orifices
    orifice_orderRPC = ['14', '38', '12', '34', '10', '1Q']

    # monset is the monitor setpoint, will be 0 unless we are sizing for a monitor regulator
    monset = 0
    if opp == "Monitor":
        if outlet_input == 0.25:
            monset = 1
        elif outlet_input < 3:
            monset = outlet_input + 1
        elif outlet_input < 5:
            monset = outlet_input + 2
        elif outlet_input <= 32:
            monset = outlet_input + 3
        else:
            monset = 35

    for prefix in ordered_prefixes:
        for orifice in orifice_orderRPC:
            reg = f"{prefix}_{orifice}"
            if reg in result:
                cap = result[reg]
                if will_work(cap, reg, orifice_maxRPC(reg)) == "Yes":
                    
                    if model_input == "243-RPC-B":
                        model = model_labelsRPC[prefix]
                    else:
                        model = model_input

                    # RPC-B always 2"
                    if model == "243-RPC-B" or pipesize_input == 0:
                        body = '2"'
                    else:
                        body = pipesize_input

                    match = {
                        'reg' : reg,
                        'model': model,
                        'diap': None,
                        'body': body,
                        'orifice': orifice_typeRPC(reg),
                        'seat': None,
                        'color': spring_RPC(outlet_input)['color'],
                        'range': spring_RPC(outlet_input)['range'],
                        'capacity': cap,
                        'opp': opp,
                        'mon_color': spring_RPC(monset)['color'] if opp == "Monitor" else None,
                        'mon_range': spring_RPC(monset)['range'] if opp == "Monitor" else None,
                    }
                    return match


